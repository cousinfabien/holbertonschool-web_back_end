#!/usr/bin/env python3
"""
This module provides a type-annotated function to convert
a floating-point number into its string representation.
"""


def to_str(n: float) -> str:
    """
    Converts a float to a string.

    Args:
        n (float): The floating-point number to convert.

    Returns:
        str: The string representation of the float.
    """
    return str(n)
